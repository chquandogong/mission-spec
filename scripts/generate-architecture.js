// Generates .mission/architecture/ARCHITECTURE_COMPUTED.yaml from src/ AST analysis.
// Deterministic: same src/ → same output. Used by pre-commit and CI to detect
// drift between code and the hand-curated ARCHITECTURE_CURRENT.yaml.
//
// Usage:
//   node scripts/generate-architecture.js
//       Write .mission/architecture/ARCHITECTURE_COMPUTED.yaml.
//
//   node scripts/generate-architecture.js --check
//       Regenerate and compare against the committed COMPUTED file. Exit 1 on drift.
//
//   node scripts/generate-architecture.js --verify-current
//       Compare against the hand-curated ARCHITECTURE_CURRENT.yaml. Exit 1 if the
//       mechanical fields (module ids, paths, depends_on, public_api) diverge.

import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { parse, stringify } from "yaml";
import { extractArchitecture } from "../dist/core/architecture-extractor.js";

// F-3 (v1.16.11 / PROJECT_REVIEW_SNAPSHOT_V1.16.7 Rev.4 Codex §3): recursive
// leaf-path comparison for nested conditional exports. Returns a list of
// mismatch strings rooted at the given path. Empty list means equal.
// Primitive inequality produces one entry with the full path; object type
// vs primitive mismatch is reported as a shape mismatch at that path;
// missing/extra subkeys are reported per subkey.
function diffExportsLeaf(pkgValue, regValue, path) {
  const mismatches = [];
  const pkgIsObj =
    pkgValue && typeof pkgValue === "object" && !Array.isArray(pkgValue);
  const regIsObj =
    regValue && typeof regValue === "object" && !Array.isArray(regValue);

  if (!pkgIsObj && !regIsObj) {
    if (pkgValue !== regValue) {
      mismatches.push(
        `API_REGISTRY.yaml public_api.package_exports${path} mismatch: registry '${regValue}', package.json '${pkgValue}'`,
      );
    }
    return mismatches;
  }

  if (pkgIsObj !== regIsObj) {
    mismatches.push(
      `API_REGISTRY.yaml public_api.package_exports${path} shape mismatch: registry ${regIsObj ? "object" : typeof regValue}, package.json ${pkgIsObj ? "object" : typeof pkgValue}`,
    );
    return mismatches;
  }

  // Both are objects — compare subkeys recursively
  const pkgKeys = Object.keys(pkgValue).sort();
  const regKeys = Object.keys(regValue).sort();
  const missing = pkgKeys.filter((k) => !regKeys.includes(k));
  const extra = regKeys.filter((k) => !pkgKeys.includes(k));
  if (missing.length > 0) {
    mismatches.push(
      `API_REGISTRY.yaml public_api.package_exports${path} missing subkeys from package.json: ${missing.join(", ")}`,
    );
  }
  if (extra.length > 0) {
    mismatches.push(
      `API_REGISTRY.yaml public_api.package_exports${path} has extra subkeys not in package.json: ${extra.join(", ")}`,
    );
  }
  for (const k of pkgKeys.filter((x) => regKeys.includes(x))) {
    const sub = diffExportsLeaf(pkgValue[k], regValue[k], `${path}.${k}`);
    for (const m of sub) mismatches.push(m);
  }
  return mismatches;
}

const projectDir = process.cwd();
const args = process.argv.slice(2);
const outputPath = join(
  projectDir,
  ".mission",
  "architecture",
  "ARCHITECTURE_COMPUTED.yaml",
);
const currentPath = join(
  projectDir,
  ".mission",
  "architecture",
  "ARCHITECTURE_CURRENT.yaml",
);
const apiRegistryPath = join(
  projectDir,
  ".mission",
  "interfaces",
  "API_REGISTRY.yaml",
);

function extractedToYamlModel(extracted) {
  return {
    generated_from: "src/",
    generator: "scripts/generate-architecture.js",
    modules: extracted.modules.map((m) => ({
      id: m.id,
      path: m.path,
      layer: m.layer,
      depends_on: m.depends_on,
      exports: m.exports,
    })),
    edges: extracted.edges,
    public_api: extracted.public_api,
  };
}

function renderYaml(model) {
  const header = [
    "# Mission Spec — Architecture (auto-generated, DO NOT EDIT BY HAND)",
    "# Source of truth: src/ (regenerated by scripts/generate-architecture.js)",
    "# Drift between this file and src/ is caught by the pre-commit hook and CI.",
    "",
  ].join("\n");
  return header + stringify(model, { lineWidth: 120 });
}

function writeComputed() {
  const extracted = extractArchitecture(projectDir);
  const model = extractedToYamlModel(extracted);
  const content = renderYaml(model);
  mkdirSync(dirname(outputPath), { recursive: true });
  writeFileSync(outputPath, content);
  return { outputPath, content, moduleCount: extracted.modules.length };
}

function loadYaml(path) {
  return parse(readFileSync(path, "utf-8"));
}

function checkMode() {
  if (!existsSync(outputPath)) {
    console.error(
      `ARCHITECTURE_COMPUTED.yaml missing at ${outputPath}. ` +
        "Run `npm run arch-sync` to generate it.",
    );
    process.exit(1);
  }
  const committed = readFileSync(outputPath, "utf-8");
  const extracted = extractArchitecture(projectDir);
  const fresh = renderYaml(extractedToYamlModel(extracted));
  if (committed === fresh) {
    console.log("Architecture computed is in sync with src/.");
    return;
  }
  console.error(
    "Architecture drift detected between src/ and ARCHITECTURE_COMPUTED.yaml.\n" +
      "Run `npm run arch-sync` and commit the regenerated file.",
  );
  process.exit(1);
}

function verifyCurrentMode() {
  if (!existsSync(currentPath)) {
    console.error(`ARCHITECTURE_CURRENT.yaml missing at ${currentPath}.`);
    process.exit(1);
  }
  const current = loadYaml(currentPath);
  const extracted = extractArchitecture(projectDir);

  const mismatches = [];
  const currentModules = Array.isArray(current?.modules) ? current.modules : [];
  const currentById = new Map(currentModules.map((m) => [m.id, m]));
  const extractedById = new Map(extracted.modules.map((m) => [m.id, m]));

  for (const id of extractedById.keys()) {
    if (!currentById.has(id)) {
      mismatches.push(
        `module '${id}' present in src/ but missing from ARCHITECTURE_CURRENT.yaml`,
      );
    }
  }
  for (const id of currentById.keys()) {
    if (!extractedById.has(id)) {
      mismatches.push(
        `module '${id}' listed in ARCHITECTURE_CURRENT.yaml but not found in src/`,
      );
    }
  }
  for (const [id, extractedMod] of extractedById) {
    const curMod = currentById.get(id);
    if (!curMod) continue;
    if (curMod.path && curMod.path !== extractedMod.path) {
      mismatches.push(
        `module '${id}' path mismatch: current=${curMod.path}, src=${extractedMod.path}`,
      );
    }
    const curDeps = Array.isArray(curMod.depends_on)
      ? [...curMod.depends_on].sort()
      : [];
    const srcDeps = [...extractedMod.depends_on].sort();
    const missing = srcDeps.filter((d) => !curDeps.includes(d));
    const extra = curDeps.filter((d) => !srcDeps.includes(d));
    if (missing.length > 0) {
      mismatches.push(
        `module '${id}' depends_on missing entries from ARCHITECTURE_CURRENT.yaml: ${missing.join(", ")}`,
      );
    }
    if (extra.length > 0) {
      mismatches.push(
        `module '${id}' depends_on has extra entries not found in src/: ${extra.join(", ")}`,
      );
    }
  }

  if (existsSync(apiRegistryPath)) {
    const apiRegistry = loadYaml(apiRegistryPath);
    const registryFunctions = Array.isArray(apiRegistry?.public_api?.functions)
      ? apiRegistry.public_api.functions
          .map((entry) => (typeof entry?.name === "string" ? entry.name : null))
          .filter(Boolean)
          .sort()
      : [];
    const extractedFunctions = [...extracted.public_api.functions].sort();
    const missingFunctions = extractedFunctions.filter(
      (name) => !registryFunctions.includes(name),
    );
    const extraFunctions = registryFunctions.filter(
      (name) => !extractedFunctions.includes(name),
    );

    if (missingFunctions.length > 0) {
      mismatches.push(
        `API_REGISTRY.yaml public_api.functions missing entries from src/index.ts: ${missingFunctions.join(", ")}`,
      );
    }
    if (extraFunctions.length > 0) {
      mismatches.push(
        `API_REGISTRY.yaml public_api.functions has extra entries not exported by src/index.ts: ${extraFunctions.join(", ")}`,
      );
    }

    // package_exports drift (D-2 key-set + E-5 value-shape): compare
    // API_REGISTRY.public_api.package_exports against package.json's exports
    // map. Key set is the D-2 check; value shape (types/import paths, other
    // conditional keys) is the E-5 extension.
    const packageJsonPath = join(projectDir, "package.json");
    if (existsSync(packageJsonPath)) {
      let pkg;
      try {
        pkg = JSON.parse(readFileSync(packageJsonPath, "utf-8"));
      } catch (err) {
        mismatches.push(
          `package.json parse error (${err instanceof Error ? err.message : String(err)})`,
        );
        pkg = null;
      }
      if (pkg) {
        const pkgExportsObj =
          pkg.exports &&
          typeof pkg.exports === "object" &&
          !Array.isArray(pkg.exports)
            ? pkg.exports
            : {};
        const registryExportsObj =
          apiRegistry?.public_api?.package_exports &&
          typeof apiRegistry.public_api.package_exports === "object" &&
          !Array.isArray(apiRegistry.public_api.package_exports)
            ? apiRegistry.public_api.package_exports
            : {};
        const pkgExportKeys = Object.keys(pkgExportsObj).sort();
        const registryExportKeys = Object.keys(registryExportsObj).sort();
        const missingInRegistry = pkgExportKeys.filter(
          (k) => !registryExportKeys.includes(k),
        );
        const extraInRegistry = registryExportKeys.filter(
          (k) => !pkgExportKeys.includes(k),
        );

        if (missingInRegistry.length > 0) {
          mismatches.push(
            `API_REGISTRY.yaml public_api.package_exports missing keys from package.json: ${missingInRegistry.join(", ")}`,
          );
        }
        if (extraInRegistry.length > 0) {
          mismatches.push(
            `API_REGISTRY.yaml public_api.package_exports has extra keys not in package.json: ${extraInRegistry.join(", ")}`,
          );
        }

        // E-5: for keys present on both sides, compare the value shape.
        // package.json's exports map entries are either a plain string
        // (shortcut form) or an object of conditions (types/import/require/default).
        const commonKeys = pkgExportKeys.filter((k) =>
          registryExportKeys.includes(k),
        );
        for (const key of commonKeys) {
          const pkgEntry = pkgExportsObj[key];
          const regEntry = registryExportsObj[key];
          const pkgIsObj =
            pkgEntry &&
            typeof pkgEntry === "object" &&
            !Array.isArray(pkgEntry);
          const regIsObj =
            regEntry &&
            typeof regEntry === "object" &&
            !Array.isArray(regEntry);

          if (typeof pkgEntry === "string" && typeof regEntry === "string") {
            if (pkgEntry !== regEntry) {
              mismatches.push(
                `API_REGISTRY.yaml public_api.package_exports['${key}'] value mismatch: registry '${regEntry}', package.json '${pkgEntry}'`,
              );
            }
            continue;
          }

          if (pkgIsObj && regIsObj) {
            const pkgSubKeys = Object.keys(pkgEntry).sort();
            const regSubKeys = Object.keys(regEntry).sort();
            const missingSubKeys = pkgSubKeys.filter(
              (k) => !regSubKeys.includes(k),
            );
            const extraSubKeys = regSubKeys.filter(
              (k) => !pkgSubKeys.includes(k),
            );
            if (missingSubKeys.length > 0) {
              mismatches.push(
                `API_REGISTRY.yaml public_api.package_exports['${key}'] missing subkeys from package.json: ${missingSubKeys.join(", ")}`,
              );
            }
            if (extraSubKeys.length > 0) {
              mismatches.push(
                `API_REGISTRY.yaml public_api.package_exports['${key}'] has extra subkeys not in package.json: ${extraSubKeys.join(", ")}`,
              );
            }
            // F-3 (v1.16.11 / Rev.4 Codex §3): descend into nested conditional
            // exports. Before v1.16.11 this used `pkgEntry[sk] !== regEntry[sk]`
            // which failed reference-equality for every object, producing the
            // `[object Object]` false-drift Codex reproduced. Now report
            // leaf-path mismatches like `['.'].node.import`.
            for (const sk of pkgSubKeys.filter((k) => regSubKeys.includes(k))) {
              const leafMismatches = diffExportsLeaf(
                pkgEntry[sk],
                regEntry[sk],
                `['${key}'].${sk}`,
              );
              for (const m of leafMismatches) mismatches.push(m);
            }
            continue;
          }

          // One side is string, the other is object — structural drift.
          mismatches.push(
            `API_REGISTRY.yaml public_api.package_exports['${key}'] shape mismatch: registry is ${regIsObj ? "object" : typeof regEntry}, package.json is ${pkgIsObj ? "object" : typeof pkgEntry}`,
          );
        }
      }
    }
  }

  if (mismatches.length === 0) {
    console.log(
      "ARCHITECTURE_CURRENT.yaml and API_REGISTRY.yaml are in sync with src/.",
    );
    return;
  }
  console.error("ARCHITECTURE_CURRENT.yaml is out of sync with src/:");
  for (const msg of mismatches) console.error(`  - ${msg}`);
  console.error(
    "\nUpdate ARCHITECTURE_CURRENT.yaml by hand, or run `npm run arch-sync` to regenerate ARCHITECTURE_COMPUTED.yaml as reference.",
  );
  process.exit(1);
}

if (args.includes("--check")) {
  checkMode();
} else if (args.includes("--verify-current")) {
  verifyCurrentMode();
} else {
  const { outputPath: out, moduleCount } = writeComputed();
  console.log(`Wrote ${out} (${moduleCount} modules)`);
}
