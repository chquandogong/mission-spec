// Generates .mission/architecture/ARCHITECTURE_COMPUTED.yaml from src/ AST analysis.
// Deterministic: same src/ → same output. Used by pre-commit and CI to detect
// drift between code and the hand-curated ARCHITECTURE_CURRENT.yaml.
//
// Usage:
//   node scripts/generate-architecture.js
//       Write .mission/architecture/ARCHITECTURE_COMPUTED.yaml.
//
//   node scripts/generate-architecture.js --check
//       Regenerate and compare against the committed COMPUTED file. Exit 1 on drift.
//
//   node scripts/generate-architecture.js --verify-current
//       Compare against the hand-curated ARCHITECTURE_CURRENT.yaml. Exit 1 if the
//       mechanical fields (module ids, paths, depends_on, public_api) diverge.

import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { parse, stringify } from "yaml";
import { extractArchitecture } from "../dist/core/architecture-extractor.js";

const projectDir = process.cwd();
const args = process.argv.slice(2);
const outputPath = join(
  projectDir,
  ".mission",
  "architecture",
  "ARCHITECTURE_COMPUTED.yaml",
);
const currentPath = join(
  projectDir,
  ".mission",
  "architecture",
  "ARCHITECTURE_CURRENT.yaml",
);

function extractedToYamlModel(extracted) {
  return {
    generated_from: "src/",
    generator: "scripts/generate-architecture.js",
    modules: extracted.modules.map((m) => ({
      id: m.id,
      path: m.path,
      layer: m.layer,
      depends_on: m.depends_on,
      exports: m.exports,
    })),
    edges: extracted.edges,
    public_api: extracted.public_api,
  };
}

function renderYaml(model) {
  const header = [
    "# Mission Spec — Architecture (auto-generated, DO NOT EDIT BY HAND)",
    "# Source of truth: src/ (regenerated by scripts/generate-architecture.js)",
    "# Drift between this file and src/ is caught by the pre-commit hook and CI.",
    "",
  ].join("\n");
  return header + stringify(model, { lineWidth: 120 });
}

function writeComputed() {
  const extracted = extractArchitecture(projectDir);
  const model = extractedToYamlModel(extracted);
  const content = renderYaml(model);
  mkdirSync(dirname(outputPath), { recursive: true });
  writeFileSync(outputPath, content);
  return { outputPath, content, moduleCount: extracted.modules.length };
}

function loadYaml(path) {
  return parse(readFileSync(path, "utf-8"));
}

function checkMode() {
  if (!existsSync(outputPath)) {
    console.error(
      `ARCHITECTURE_COMPUTED.yaml missing at ${outputPath}. ` +
        "Run `npm run arch-sync` to generate it.",
    );
    process.exit(1);
  }
  const committed = readFileSync(outputPath, "utf-8");
  const extracted = extractArchitecture(projectDir);
  const fresh = renderYaml(extractedToYamlModel(extracted));
  if (committed === fresh) {
    console.log("Architecture computed is in sync with src/.");
    return;
  }
  console.error(
    "Architecture drift detected between src/ and ARCHITECTURE_COMPUTED.yaml.\n" +
      "Run `npm run arch-sync` and commit the regenerated file.",
  );
  process.exit(1);
}

function verifyCurrentMode() {
  if (!existsSync(currentPath)) {
    console.error(`ARCHITECTURE_CURRENT.yaml missing at ${currentPath}.`);
    process.exit(1);
  }
  const current = loadYaml(currentPath);
  const extracted = extractArchitecture(projectDir);

  const mismatches = [];
  const currentModules = Array.isArray(current?.modules) ? current.modules : [];
  const currentById = new Map(currentModules.map((m) => [m.id, m]));
  const extractedById = new Map(extracted.modules.map((m) => [m.id, m]));

  for (const id of extractedById.keys()) {
    if (!currentById.has(id)) {
      mismatches.push(
        `module '${id}' present in src/ but missing from ARCHITECTURE_CURRENT.yaml`,
      );
    }
  }
  for (const id of currentById.keys()) {
    if (!extractedById.has(id)) {
      mismatches.push(
        `module '${id}' listed in ARCHITECTURE_CURRENT.yaml but not found in src/`,
      );
    }
  }
  for (const [id, extractedMod] of extractedById) {
    const curMod = currentById.get(id);
    if (!curMod) continue;
    if (curMod.path && curMod.path !== extractedMod.path) {
      mismatches.push(
        `module '${id}' path mismatch: current=${curMod.path}, src=${extractedMod.path}`,
      );
    }
    const curDeps = Array.isArray(curMod.depends_on)
      ? [...curMod.depends_on].sort()
      : [];
    const srcDeps = [...extractedMod.depends_on].sort();
    const missing = srcDeps.filter((d) => !curDeps.includes(d));
    if (missing.length > 0) {
      mismatches.push(
        `module '${id}' depends_on missing entries from ARCHITECTURE_CURRENT.yaml: ${missing.join(", ")}`,
      );
    }
  }

  if (mismatches.length === 0) {
    console.log("ARCHITECTURE_CURRENT.yaml module list is in sync with src/.");
    return;
  }
  console.error("ARCHITECTURE_CURRENT.yaml is out of sync with src/:");
  for (const msg of mismatches) console.error(`  - ${msg}`);
  console.error(
    "\nUpdate ARCHITECTURE_CURRENT.yaml by hand, or run `npm run arch-sync` to regenerate ARCHITECTURE_COMPUTED.yaml as reference.",
  );
  process.exit(1);
}

if (args.includes("--check")) {
  checkMode();
} else if (args.includes("--verify-current")) {
  verifyCurrentMode();
} else {
  const { outputPath: out, moduleCount } = writeComputed();
  console.log(`Wrote ${out} (${moduleCount} modules)`);
}
